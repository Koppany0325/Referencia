    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String query = "SELECT * FROM logintest WHERE username=? and password=? and type=?";
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/logintest", "root","");
            pst=con.prepareStatement(query);
            pst.setString(1, username.getText());
            pst.setString(2, String.valueOf(password.getPassword()));
            pst.setString(3, String.valueOf(combobox.getSelectedItem()));
            rs=pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(this,"You are loged in as "+rs.getString("type"));
                if(combobox.getSelectedIndex()==0)
                {
                    admin a = new admin();
                    a.setVisible(true);
                    this.setVisible(false);
                }else{
                    user u = new user(username.getText());
                    u.setVisible(true);
                    this.setVisible(false);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Not matching username and password");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

